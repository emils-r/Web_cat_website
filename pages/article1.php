    <div class="article__paragraph">
        <p>
            Your cat knows what’s up. Even if she doesn’t run and hide at the sight of her carrier, there’s no way she’s going into that thing without a fight. But the fight stresses her out and can leave you frustrated and scratched up—not to mention late for the vet. Enter the kitty burrito.<br>
            Basically, you need to restrain your cat’s limbs, both to protect yourself from claws and to stop her from grabbing hold of the outside of the carrier. (How is such a small animal so strong??) Being wrapped up can also help soothe your cat, like swaddling a baby or putting an anxious dog into a Thundershirt. It’s simple:<br>
            <ol>
                <li>Lay a towel down on a flat surface. (A couch or table may be easier than the floor.)</li>
                <li>Set the carrier nearby, vertically, with the opening facing up. (I like to brace it in the corner of my couch.)</li>
                <li>Get your cat. (Okay, this can be tricky. Try coaxing her with treats or food, but sometimes you have to gently get her by the scruff of her neck. Just do your best not to panic her.)</li>
                <li>Set your cat on the towel, with her face by the top edge.</li>
                <li>Gently wrap the towel around your cat, making her the filling of the burrito. Tuck the bottom around her legs (so none of the filling leaks out).</li>
                <li>Slip the entire burrito into the cat carrier. Once you close the carrier, your cat should have enough room to orient herself properly. You can leave the towel in, but if it’s gotten tangled or is in a weird spot, pull it out before you shut the door.</li>
                <li>If your cat’s up for it, offer her a treat through the door of the carrier, as a reward and a peace offering.</li>
            </ol>
            Kitty burritos can also work for soothing and restraining your cat while you clip her nails or give her medicine, and they help feral or scared cats acclimate to human contact. And guac isn’t even extra.<br>
        </p>

